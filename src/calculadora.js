
export const somar = ( numberone, numbertwo) => {
  const soma =  Number(numberone) + Number(numbertwo)
    return soma
}

export const subtrair = (numberone, numbertwo) => {
    const sub =  Number(numberone) - Number(numbertwo)
    return sub
}

export const dividir = (numberone, numbertwo) => {
    const div =  Number(numberone) / Number(numbertwo)
    return div
}

export const multiplicar = (numberone, numbertwo) => {
    const mult =  Number(numberone) * Number(numbertwo)
    return mult
}
export const porcentagem = (numberone, numbertwo) => {
    const porce = Number(numberone) / 100
    return porce
}


